function escapeRegex(text) {
  return text.replace(/[-\/\\^$*+?.()|[\]{}]/g, "\\$&"); // Escape special characters, including commas
}

// Modify highlightTextNodes to handle word boundaries correctly
function highlightTextNodes(node) {
  if (node.nodeType === Node.TEXT_NODE) {
    Object.keys(companies).forEach((companyName) => {
      const escapedCompanyName = escapeRegex(companyName);
      const escapedCompanyType = escapeRegex(companies[companyName]);
      const regex = new RegExp(`\\b${escapedCompanyName}\\b`, 'gi');

      let match;
      while ((match = regex.exec(node.textContent)) !== null) {
        const spanElement = document.createElement("span");
        spanElement.className = `${escapedCompanyType}-highlighted`;
        const matchedText = match[0];
        const index = match.index;
        spanElement.textContent = matchedText;
        node.splitText(index);
        node.nextSibling.nodeValue = node.nextSibling.nodeValue.substring(matchedText.length);
        node.parentNode.insertBefore(spanElement, node.nextSibling);
        node = node.nextSibling;
      }
    });
  } else if (node.nodeType === Node.ELEMENT_NODE) {
    node.childNodes.forEach((childNode) => highlightTextNodes(childNode));
  }
}


